<template>
  <div class="albums-page scrollWrapper">
    <full-page-loader v-if="!albums.length" />
    <artwork-and-title
      v-else
      class="margin"
      v-for="album in albums"
      :item="album"
      :key="album.id"
      :size="'small'"
      :type="'library-album'"
    />
  </div>
</template>

<script>
import helpers from '../../store/helpers';
import ArtworkAndTitle from '@/components/ArtworkAndTitle.vue';
import FullPageLoader from '@/components/FullPageLoader';
import { mapActions, mapState } from 'vuex';

export default {
  name: 'albums',
  data() {
    return {};
  },
  components: {
    FullPageLoader,
    ArtworkAndTitle,
  },
  methods: {
    ...mapActions('myLibrary', {
      fetchAllItems: 'fetchAllItems',
    }),
  },
  computed: {
    ...mapState('myLibrary', {
      albums: (state) => state.albums,
    }),
  },
  created() {
    this.fetchAllItems({ refresh: false, item: 'albums', options: null, library: true });
  },
};
</script>
