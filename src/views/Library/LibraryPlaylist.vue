<template>
  <div class="albums-page scrollWrapper">
    <full-page-loader v-if="!playlists.length" />
    <artwork-and-title
      class="margin"
      v-for="playlist in playlists"
      :key="playlist.id"
      :size="'small'"
      :item="playlist"
      :type="'library-playlist'"
    />
  </div>
</template>

<script>
import helpers from '../../store/helpers';
import ArtworkAndTitle from '@/components/ArtworkAndTitle.vue';
import FullPageLoader from '@/components/FullPageLoader';
import { mapActions, mapState } from 'vuex';

export default {
  name: 'library-playlist',
  data() {
    return {};
  },
  components: {
    FullPageLoader,
    ArtworkAndTitle,
  },
  methods: {
    getUrl: helpers.getUrl,
    getType: helpers.getType,
    ...mapActions('myLibrary', {
      fetchAllItems: 'fetchAllItems',
    }),
  },
  computed: {
    ...mapState('myLibrary', {
      playlists: (state) => state.playlists,
    }),
  },
  mounted() {
    this.fetchAllItems({ refresh: false, item: 'playlists', options: null, library: true });
  },
};
</script>
